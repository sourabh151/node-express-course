// Using Event Emitter API
const server = http.createServer()
// emits request event
// subcribe to it / listen for it / respond to it
server.on('request', (req, res) => {
  res.end('Welcome')
})
console.log(server.eventNames());
server.listen(5000)

setTimeout(()=>{
  server.emit('connection');
},5000);
